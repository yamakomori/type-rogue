function renderString(text, keyPrefix) {
  const result = [];
  let plain = "";
  let index = 0;

  const flushPlain = () => {
    if (!plain) return;
    result.push(plain);
    plain = "";
  };

  while (index < text.length) {
    const match = READING_ENTRIES.find(([base]) => text.startsWith(base, index));
    if (!match) {
      plain += text[index];
      index += 1;
      continue;
    }
    flushPlain();
    const [base, reading] = match;
    result.push(
      <ruby key={`${keyPrefix}-${index}`}>
        {base}
        <rt aria-hidden="true">{reading}</rt>
      </ruby>,
    );
    index += base.length;
  }
  flushPlain();
  return result;
}

export function UiText({ children }) {
  return Children.map(children, (child, index) => {
    if (typeof child === "string" || typeof child === "number") {
      return <Fragment key={index}>{renderString(String(child), index)}</Fragment>;
    }
    return child;
  });
}

